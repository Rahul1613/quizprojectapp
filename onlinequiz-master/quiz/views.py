@login_required(login_url='studentlogin')
def take_exam_view(request, pk):
    course = models.Course.objects.get(id=pk)
    total_questions = models.Question.objects.filter(course=course).count()
    questions = models.Question.objects.filter(course=course).order_by('?')[:total_questions]
    total_marks = sum([q.marks for q in questions])
    
    if request.method == 'POST':
        pass_marks = course.total_marks * 0.4  # 40% passing criteria
        marks = 0
        wrong_answers = []
        correct_answers = []
        
        for q in questions:
            submitted_ans = request.POST.get(str(q.id))
            actual_answer = q.answer
            
            if submitted_ans == actual_answer:
                marks += q.marks
                correct_answers.append({
                    'question': q.question,
                    'submitted': submitted_ans
                })
            else:
                wrong_answers.append({
                    'question': q.question,
                    'submitted': submitted_ans,
                    'correct': actual_answer
                })
        
        percentage = (marks / total_marks) * 100
        result = models.Result.objects.create(
            student=SMODEL.Student.objects.get(user=request.user),
            exam=course,
            marks=marks
        )
        
        context = {
            'marks': marks,
            'correct_answers': correct_answers,
            'wrong_answers': wrong_answers,
            'total_marks': total_marks,
            'percentage': percentage,
            'passed': percentage >= 40
        }
        return render(request, 'quiz/exam_result.html', context)
    
    context = {
        'course': course,
        'total_questions': total_questions,
        'total_marks': total_marks,
        'time_limit_minutes': course.question_number * 2,  # 2 minutes per question
        'questions': questions
    }
    return render(request, 'quiz/take_exam.html', context)

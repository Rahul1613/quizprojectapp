@login_required(login_url='adminlogin')
@cache_page(60 * 15)  # Cache for 15 minutes
def admin_dashboard_view(request):
    cache_key = 'admin_dashboard_stats'
    stats = cache.get(cache_key)
    
    if stats is None:
        stats = {
            'total_student': SMODEL.Student.objects.count(),
            'total_teacher': TMODEL.Teacher.objects.filter(status=True).count(),
            'total_course': models.Course.objects.count(),
            'total_question': models.Question.objects.count(),
            'recent_students': SMODEL.Student.objects.order_by('-id')[:5],
            'recent_teachers': TMODEL.Teacher.objects.filter(status=True).order_by('-id')[:5],
            'recent_courses': models.Course.objects.order_by('-id')[:5],
        }
        cache.set(cache_key, stats, 300)  # Cache for 5 minutes
    
    return render(request,'quiz/admin_dashboard.html',context=stats)

